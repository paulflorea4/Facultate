function ensureSampleRooms(mysqli $conn) {
    $countResult = $conn->query('SELECT COUNT(*) AS total FROM rooms');
    $countRow = $countResult->fetch_assoc();
    if ((int) ($countRow['total'] ?? 0) > 0) {
        return 0;
    }

    $stmt = $conn->prepare('INSERT INTO rooms (room_number, room_type, capacity, price_per_night, description, status, created_at) VALUES (?, ?, ?, ?, ?, \'available\', NOW())');
    $inserted = 0;

    foreach (roomsSampleData() as $room) {
        $stmt->bind_param('ssids', $room[0], $room[1], $room[2], $room[3], $room[4]);
        $stmt->execute();
        $inserted++;
    }

    $stmt->close();
    return $inserted;
}

function ensureAdminUser(mysqli $conn, $username, $email, $password, $fullName) {
    $check = $conn->prepare('SELECT id FROM users WHERE username = ? OR email = ? LIMIT 1');
    $check->bind_param('ss', $username, $email);
    $check->execute();
    $exists = $check->get_result()->num_rows > 0;
    $check->close();

    if ($exists) {
        return 'Admin user already exists; skipping creation.';
    }

    $hash = password_hash($password, PASSWORD_BCRYPT);
    $stmt = $conn->prepare('INSERT INTO users (username, email, password, full_name, role, created_at, updated_at) VALUES (?, ?, ?, ?, \'admin\', NOW(), NOW())');
    $stmt->bind_param('ssss', $username, $email, $hash, $fullName);
    $stmt->execute();
    $stmt->close();

    return 'Admin user created successfully.';
}

function createDatabaseIfNeeded(mysqli $serverConn, $dbName) {
    $safeName = str_replace('`', '``', $dbName);
    $serverConn->query("CREATE DATABASE IF NOT EXISTS `{$safeName}` CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci");
}

function maybeCreateSqliteSchema($sqliteFile) {
    if ($sqliteFile === '') {
        return 'SQLite file path not provided; skipped.';
    }

    if (!in_array('sqlite', PDO::getAvailableDrivers(), true)) {
        return 'SQLite PDO driver is not available on this server; skipped.';
    }

    $sqliteDir = dirname($sqliteFile);
    if (!is_dir($sqliteDir) && !@mkdir($sqliteDir, 0775, true) && !is_dir($sqliteDir)) {
        return 'Could not create the SQLite directory; skipped.';
    }

    if (!file_exists($sqliteFile)) {
        @touch($sqliteFile);
    }

    try {
        $sqlitePdo = new PDO(
            'sqlite:' . $sqliteFile,
            null,
            null,
            [
                PDO::ATTR_ERRMODE => PDO::ERRMODE_EXCEPTION,
                PDO::ATTR_DEFAULT_FETCH_MODE => PDO::FETCH_ASSOC,
            ]
        );
    } catch (Throwable $e) {
        return 'Could not open the SQLite database; skipped.';
    }

    if (!file_exists(SETUP_SQLITE_SCHEMA_FILE)) {
        return 'SQLite schema file not found; skipped.';
    }

    $schemaSql = file_get_contents(SETUP_SQLITE_SCHEMA_FILE);
    $statements = preg_split('/;\s*(?:\r?\n|$)/', $schemaSql);
    foreach ($statements as $statement) {
        $trimmed = trim($statement);
        if ($trimmed !== '') {
            $sqlitePdo->exec($trimmed);
        }
    }

    @chmod($sqliteDir, 0775);
    @chmod($sqliteFile, 0664);

    return 'SQLite schema checked successfully.';
}

function handleSetup(array $input) {
    $dbHost = trim((string) ($input['db_host'] ?? ''));
    $dbUser = trim((string) ($input['db_user'] ?? ''));
    $dbPass = (string) ($input['db_pass'] ?? '');
    $dbName = trim((string) ($input['db_name'] ?? ''));
    $adminUsername = trim((string) ($input['admin_username'] ?? ''));
    $adminEmail = trim((string) ($input['admin_email'] ?? ''));
    $adminPassword = (string) ($input['admin_password'] ?? '');
    $adminFullName = trim((string) ($input['admin_fullname'] ?? ''));
    $writeConfig = !empty($input['write_config']);
    $onlySqlite = !empty($input['only_sqlite']);

    if (!$onlySqlite) {
        if ($dbHost === '' || $dbUser === '' || $dbName === '' || $adminUsername === '' || $adminEmail === '' || $adminPassword === '' || $adminFullName === '') {
            return ['ok' => false, 'message' => 'All required fields must be completed.'];
        }
    }

    if (!filter_var($adminEmail, FILTER_VALIDATE_EMAIL)) {
        return ['ok' => false, 'message' => 'The admin email address is invalid.'];
    }

    if (strlen($adminPassword) < 8) {
        return ['ok' => false, 'message' => 'The admin password must be at least 8 characters long.'];
    }

    try {
        $sqliteDbFile = __DIR__ . '/contact-types.sqlite';

        if ($onlySqlite) {
            // Only create/check the SQLite DB (MySQL already configured)
            $executedStatements = 0;
            $insertedRooms = 0;
            $adminMessage = 'Skipped MySQL operations (only SQLite requested).';
            $sqliteMessage = maybeCreateSqliteSchema($sqliteDbFile);
        } else {
            $serverConn = connectMysql($dbHost, $dbUser, $dbPass);
            createDatabaseIfNeeded($serverConn, $dbName);
            $serverConn->select_db($dbName);

            if (!file_exists(SETUP_SCHEMA_FILE)) {
                return ['ok' => false, 'message' => 'schema.sql was not found next to setup.php.'];
            }

            $schemaSql = file_get_contents(SETUP_SCHEMA_FILE);
            $createStatements = extractCreateStatements($schemaSql);
            $executedStatements = runCreateStatements($serverConn, $createStatements);
            $insertedRooms = ensureSampleRooms($serverConn);
            $adminMessage = ensureAdminUser($serverConn, $adminUsername, $adminEmail, $adminPassword, $adminFullName);
            $sqliteMessage = maybeCreateSqliteSchema($sqliteDbFile);
        }

        $baseUrl = rtrim(sprintf(
            '%s://%s%s/',
            (!empty($_SERVER['HTTPS']) && $_SERVER['HTTPS'] !== 'off') ? 'https' : 'http',
            $_SERVER['HTTP_HOST'] ?? 'localhost',
            dirname(dirname($_SERVER['SCRIPT_NAME'] ?? '/'))
        ), '/') . '/';

        $configMessage = 'server-config.php was not written.';
        if ($writeConfig) {
            $configPath = __DIR__ . '/server-config.php';
            $configData = [
                'db_host' => $dbHost,
                'db_user' => $dbUser,
                'db_pass' => $dbPass,
                'db_name' => $dbName,
                'contact_types_db_file' => $sqliteDbFile,
            ];

            $written = @file_put_contents($configPath, buildServerConfig($configData, $baseUrl));
            if ($written === false) {
                $configMessage = 'Could not write server-config.php automatically. Copy the settings from this page into back-end/server-config.php manually.';
            } else {
                $configMessage = 'server-config.php created successfully.';
            }
        }

        $serverConn->close();

        return [
            'ok' => true,
            'message' => 'Setup completed successfully.',
            'details' => [
                'Database host' => $dbHost,
                'Database name' => $dbName,
                'Database user' => $dbUser,
                'Create statements executed' => (string) $executedStatements,
                'Sample rooms inserted' => (string) $insertedRooms,
                'Admin status' => $adminMessage,
                'SQLite status' => $sqliteMessage,
                'Production config' => $configMessage,
                'Base URL' => $baseUrl,
            ],
            'baseUrl' => $baseUrl,
            'db' => [
                'host' => $dbHost,
                'user' => $dbUser,
                'pass' => $dbPass,
                'name' => $dbName,
            ],
            'sqlite_file' => $sqliteDbFile,
        ];
    } catch (Throwable $e) {
        return ['ok' => false, 'message' => 'Setup failed: ' . $e->getMessage()];
    }
}

