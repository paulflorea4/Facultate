    $lines[] = '';
    return implode(PHP_EOL, $lines) . PHP_EOL;
}

function extractCreateStatements($schemaSql) {
    $statements = preg_split('/;\s*(?:\r?\n|$)/', $schemaSql);
    $createStatements = [];

    foreach ($statements as $statement) {
        $trimmed = trim($statement);
        if ($trimmed === '') {
            continue;
        }

        if (preg_match('/^(CREATE\s+TABLE|CREATE\s+INDEX|ALTER\s+TABLE)/i', $trimmed)) {
            $createStatements[] = $trimmed;
        }
    }

    return $createStatements;
}

function runCreateStatements(mysqli $conn, array $statements) {
    $executed = 0;
    foreach ($statements as $statement) {
        $conn->query($statement);
        $executed++;
    }
    return $executed;
}

