    $lines[] = 'if (!defined(\'DB_USER\')) define(\'DB_USER\', ' . var_export($data['db_user'], true) . ');';
    $lines[] = 'if (!defined(\'DB_PASS\')) define(\'DB_PASS\', ' . var_export($data['db_pass'], true) . ');';
    $lines[] = 'if (!defined(\'DB_NAME\')) define(\'DB_NAME\', ' . var_export($data['db_name'], true) . ');';
    $lines[] = 'if (!defined(\'CONTACT_TYPES_DB_FILE\')) define(\'CONTACT_TYPES_DB_FILE\', ' . var_export($data['contact_types_db_file'], true) . ');';
    $lines[] = 'if (!defined(\'BASE_URL\')) define(\'BASE_URL\', ' . var_export($baseUrl, true) . ');';
    $lines[] = 'if (!defined(\'BACKEND_URL\')) define(\'BACKEND_URL\', BASE_URL . \'back-end/\');';
